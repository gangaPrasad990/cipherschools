import java.lang.*;
class controlflow1{
    public static void main(String args[]){
    char a='c';
    switch(a){
        case'a':
        System.out.println("I am A");
        break;
        case'b':
        System.out.println("I am B");
        break;
        case'c':
        System.out.println("I am C");
        break;
        default:
        System.out.println("I am not A,b,c");
    }

    }
}